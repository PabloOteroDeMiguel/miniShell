#include"libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	i;

	i = ft_strlen(s1) + 1;
	s2 = malloc(i);
	if (!s2)
		return (0);
	ft_strlcpy(s2, s1, i);
	return (s2);
}
