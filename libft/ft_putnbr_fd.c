#include<unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	a;
	char			c;

	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	a = n;
	if (a == 0)
	{
		write(fd, "0", 1);
	}
	if (a > 0 && a < 10)
	{
		c = a + 48;
		write(fd, &c, 1);
	}
	if (a >= 10)
	{
		ft_putnbr_fd((a / 10), fd);
		c = (a % 10 + 48);
		write(fd, &c, 1);
	}
}
